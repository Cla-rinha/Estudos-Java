public class TotalValorProdutos {
    public static void main(String[] args) {

        //Declarando as variaveis
        int item = 35;
        float itemValor = 8.50F;
        float valorTotal = item * itemValor;

        //Imprimindo valores

        System.out.println("Quantidade de itens: " + item);
        System.out.println("Valor de cada item: " + itemValor);
        System.out.println("Valor total: " + valorTotal);
    }

}
