public class NumerosPrimitivos {

    public static void main(String[] args) {

        //Valores Principais
        int myNum = 5;               // Numero inteiro
        float myFloatNum = 5.99f;    // Numero com virgula
        char myLetter = 'D';         // Um unico caracter
        boolean myBool = true;       // Verdadeiro ou Falso
        String myText = "Hello";     // Texto

        System.out.println(myNum);
        System.out.println(myFloatNum);
        System.out.println(myLetter);
        System.out.println(myBool);
        System.out.println(myText);

        //Valores Inteiros
        byte myByte = 100;
        System.out.println(myByte);

        short myShort = 5000;
        System.out.println(myShort);

        int myInt = 100000;
        System.out.println(myInt);

        long myLong = 15000000000L;
        System.out.println(myLong);

        //Valores de ponto flutuante
        float myFloat = 5.75f;
        System.out.println(myFloat);

        double myDouble = 19.99d;
        System.out.println(myDouble);

        //Valores de Verdadeiro ou Falso
        boolean isJavaFun = true;
        boolean isFishTasty = false;
        System.out.println(isJavaFun);
        System.out.println(isFishTasty);

        //Valor Unico
        char myGrade = 'B';
        System.out.println(myGrade);

        //Valor de texto
        String myString = "Ol√° Mundo!";
        System.out.println(myString);

    }
}
