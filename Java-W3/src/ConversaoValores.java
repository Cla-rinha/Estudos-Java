public class ConversaoValores {
    public static void main(String[] args) {
        //Conversao maior para o menor
        int myValor = 9;
        double myDouble = myValor;

        System.out.println(myValor);
        System.out.println(myDouble);

        //Conversao menor para o maior
        double myValor2 = 9.78F;
        int myInt2 = (int) myValor2;

        System.out.println(myValor2);
        System.out.println(myInt2);
    }
}
